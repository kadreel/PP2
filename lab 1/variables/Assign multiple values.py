#Many values to multiple variables
x, y, z = "Orange", "Banana", "Cherry"
print(x) #prints Orange
print(y) #prints Banana
print(z) #prints Cherry

x = y = z = "Orange"
print(x) #all three print Orange
print(y)
print(z)

fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print(x) #prints apple
print(y) #prints banana
print(z) #prints cherry